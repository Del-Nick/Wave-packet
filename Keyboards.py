def back_keyboard(user: User, button: str = None):
    """
    –ö–ª–∞–≤–∏–∞—Ç—É—Ä–∞ —Ç–æ–ª—å–∫–æ —Å –∫–Ω–æ–ø–∫–æ–π –Ω–∞–∑–∞–¥. –ú–µ–Ω—è–µ—Ç –ø–æ—Å–ª–µ–¥–Ω–µ–µ —Å–ª–æ–≤–æ –≤ user.action –Ω–∞ start
    :return: –û–±—ä–µ–∫—Ç –∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã
    """
    keyboard = InlineKeyboardBuilder()

    _ = user.action.split("->")[0]
    if 'science_group' in _ or 'about' in _:
        user.action = 'start'
    else:
        user.action = f'{_}->start'

    if button is not None:
        button = InlineKeyboardButton(
            text=button,
            callback_data=button)

        keyboard.row(InlineKeyboardButton(
            text="–ù–∞–∑–∞–¥",
            callback_data=user.action),
            button)

    else:
        keyboard.row(InlineKeyboardButton(
            text="–ù–∞–∑–∞–¥",
            callback_data=user.action))

    if user.admin:
        keyboard.row(InlineKeyboardButton(
            text="–£–¥–∞–ª–∏—Ç—å –º–µ–Ω—è",
            callback_data='delete_user'))

    return keyboard


